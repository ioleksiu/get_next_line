#include <string.h>

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	char	*src;
	size_t	i;

	i = 0;
	src = s1;
	while (*s1)
		s1++;
	while (*s2 && i++ != n)
		*(s1++) = *(s2++);
	*s1 = '\0';
	return (src);
}
